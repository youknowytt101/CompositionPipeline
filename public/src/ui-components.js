const fallbackIcon = ['<circle cx="8" cy="8" r="6" />'];

function svgForIcon(name) {
  return `
    <svg viewBox="0 0 16 16" aria-hidden="true" part="svg">
      ${(iconPaths[name] ?? fallbackIcon).join("")}
    </svg>
  `;
}

if (!customElements.get("sl-icon")) {
  customElements.define("sl-icon", class LocalIcon extends HTMLElement {
    static get observedAttributes() {
      return ["name"];
    }

    constructor() {
      super();
      this.attachShadow({ mode: "open" });
    }

    connectedCallback() {
      this.render();
    }

    attributeChangedCallback() {
      this.render();
    }

    get name() {
      return this.getAttribute("name") ?? "";
    }

    set name(value) {
      this.setAttribute("name", value);
    }

    render() {
      if (!this.shadowRoot) {
        return;
      }

      this.shadowRoot.innerHTML = `
        <style>
          :host {
            display: inline-grid;
            width: 1em;
            height: 1em;
            place-items: center;
          }

          svg {
            width: 1em;
            height: 1em;
            fill: currentColor;
            stroke: none;
          }
        </style>
        ${svgForIcon(this.name)}
      `;
    }
  });
}

